package semana2;

public class Point {

    private double x;
    private double y;

    //1
    public Point() {
        this.x = 0;
        this.y = 0;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public void setX(double x) {
        this.x = x;;
    }

    public void setY(double y) {
        this.y = y;
    }
}
